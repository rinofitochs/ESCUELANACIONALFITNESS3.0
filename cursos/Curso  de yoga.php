Función cognitiva: El yoga ayudó a mejorar la ejecución de tareas relacionadas con la memoria, el tiempo de reacción entre cambios de tareas y mayor precisión en su ejecución (estudio).<br>

Bienestar emocional: Algunos estudios han detectado efectos positivos del yoga en lo que se denomina “positive affect“, la medida en que un individuo experimenta subjetivamente estados de ánimo positivos como la alegría, el interés y el estado de alerta. Aunque no está del todo claro el mecanismo, la práctica del yoga podría guardar relación con una mayor centralidad y conectividad neuronal del núcleo caudado con el resto del cerebro (fuente).<br>

Embarazo, feto y puerperio: El yoga prenatal en sus diferentes modalidades mostró (aquí) relación con una menor incidenca de desórdenes prenatales, menos dolor, menos estrés, mejores índices de socialización, más duración del embarazo, mayor diámetro parietal del feto, mayor longitud del fémur, mayor peso fetal y circunferencia craneal y mejor circulación sanguínea del feto. El yoga también mostró funcionar mejor que otras intervenciones —masajes, ejercicio, apoyo social…— a la hora de reducir la depresión y ansiedad preparto, problemas que afectan a entre el 10 y 20 por ciento de las embarazadas (metaanalásis); también podría ser clínicamente relevante en el tratamiento de la dramática depresión postparto (fuente).<br>

