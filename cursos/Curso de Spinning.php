    <h3> </h3>
    <p class="wow fadeInLeft delay-05s" style="font-size:16px">La investigación llevada a cabo por Hausken et al. (8) concluye que si el objetivo de los principiantes en la práctica deportiva es conseguir el mayor número de Kilocalorías quemadas posible y una mejora de la capacidad aeróbica, el spinning es la actividad física indicada. De igual manera se ha mostrado eficaz para trabajar la fuerza del core (10). Además, indicar que el consumo de energía y la frecuencia cardíaca es muy diferente en sesiones de zumba, spinning y de carrera (5). También, el spinning, ha demostrado su eficacia como método para trabajar con personas con sobrepeso y/o obesidad (18)<br>

Uno de los principales problemas de las sesiones de spinning es que pueden ocasionar rabdomiólisis en los usuarios  debido a la contracción muscular que se produce en este ejercicio y a la percepción disminuida del esfuerzo, al ser una actividad realizada en grupo, estimulada ambientalmente y alentada por un monitor  (4, 20). 
<br>También se han detectado casos de usuarios con dolores en la zona pélvica después de realizar sesiones de esta modalidad de actividad física (6). De igual manera otros problemas comunes durante las sesiones de spinning son los derivados de la deshidratación de los usuarios; ocasionada por el esfuerzo de la actividad física (21), en los hombres más que en las mujeres debido a una mayor sudoración durante la actividad (9).
<br> Si hablamos de sesiones de gran duración (2 horas), es más efectiva la hidratación con bebidas que contengan hidratos de carbono frente a beber solo agua (11) para combatirla. 
<br>Otra lesión, menos frecuente, que se ha registrado es la fractura de la columna cervical (19). Por último, pero no menos peligroso, los usuarios de spinning tienden a subestimar e infravalorar los esfuerzos que realizan durante las sesiones (14).
<br>



El estudio realizado por Furjan et al. (7) señala que los programas de entrenamiento interválico y los de spinning desarrollan y entrenan las mismas capacidades físicas. Si bien, se ha comprobado que la posición corporal durante las sesiones de spinning influye, y mucho, en la consecución de los objetivos establecidos previamente (16). Por último, indicar que se ha demostrado que la práctica de spinning mejora la capacidad de caminar en personas con osteoartritis (17).<br>

Conclusión sobre el spinning
<br>
El spinning es una actividad física con un alto grado de fidelización y de disfrute por parte de sus participantes (13).
<br>
Los estudios demuestran que, para deportistas y personas novatas, las sesiones de spinning deber considerarse como ejercicio físico de alta intensidad (1, 12, 15) el cual provoca grandes adaptaciones en el sistema cardiorrepiratorio (2).
<br>
Por lo que no es aconsejable para individuos sedentarios o de edades avanzadas, los cuales buscan actividades físicas más recreacionales (3).
